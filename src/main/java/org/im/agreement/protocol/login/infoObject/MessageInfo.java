package org.im.agreement.protocol.login.infoObject;

public class MessageInfo {

    private String bufId;
    private String bufMessage;
    private String bufDate;

    public String getBufId() {
        return bufId;
    }

    public void setBufId(String bufId) {
        this.bufId = bufId;
    }

    public String getBufMessage() {
        return bufMessage;
    }

    public void setBufMessage(String bufMessage) {
        this.bufMessage = bufMessage;
    }

    public String getBufDate() {
        return bufDate;
    }

    public void setBufDate(String bufDate) {
        this.bufDate = bufDate;
    }
}
